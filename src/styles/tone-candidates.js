/*
 * 分類色候選集：18 對「淡底＋深墨」，原本是空氣污染防制費頁自帶的識別色。
 * 每個物件的開頭都帶 token-exempt 標記：驗證器的豁免範圍是「從標記那行到下一個
 * `};`」，所以標記要逐個物件寫，寫在檔頭只會豁免到第一個物件為止。
 *
 * 為什麼留著：tokens.css 的 Layer-0 目前只有 8 對，而單是空污費一頁就要用到 7 個
 * 分類槽。這 18 對是一組現成的、已經被看過而且好看的擴充候選，不是垃圾。
 *
 * ── 它為什麼好看（2026-07-20 實算 OKLCH，不是憑感覺）─────────────────────────
 *
 * 1. 每一對的底色與墨色是同一個色相。色相差中位數約 8°，18 對裡有 14 對在 11°
 *    以內。這正是 Notion 模型要求的「同色相的淡底＋深墨」。
 *
 * 2. 墨色明度全部落在 L 0.388–0.524，中心約 0.45——「看起來一樣亮」，這是和諧感
 *    的來源。而色相鋪滿整個色環（6° 32° 37° 47° 63° 71° 88° 100° 116° 141° 153°
 *    162° 208° 251° 290° 293° 297° 317°），紅橘黃綠青藍紫繞完一圈。
 *    明度齊一 ＋ 彩度中低 ＋ 色相多樣，就是 DESIGN.md 色彩哲學第 0 條那條原理。
 *
 * 3. 它靠什麼做到明度齊一：hex 位元組的量化網格。所有墨色通道值都取自
 *    `0x30 0x38 0x40 0x48 0x50 0x58 0x60 0x68 0x70 0x78` 這把每階 8 的梯子
 *    （底色那組是每階 4，0xa4–0xe8）。通道值鎖在同一把梯子上，感知明度自然落進
 *    窄帶。土法，但有效，而且比事後拿 OKLCH 逐個修省事——這是本檔最值得學走的
 *    一招。
 *
 * ── 直接進 tokens.css 之前要處理的四處超標（同樣是實算）────────────────────
 *
 * 站內 validator（scripts/validate-color-system.mjs）的帶寬比這組嚴：
 *   - 墨色明度極差 0.136，站內上限 0.10。
 *   - VOCs 墨色 #50388a 彩度 0.1309，站內上限 0.13（剛好破線）。
 *   - 前四對（污染物）的底色 L 0.760–0.826，站內 -bg 帶要求 0.90–0.97——因為它們
 *     原本是實色色塊不是淡底，性質本來就不同，要當 -bg 用必須先提亮。
 *   - 底色彩度最高 0.064，站內 -bg 上限 0.035。
 * 也就是說：**照抄進 Layer-0 會讓 build 失敗**。要用就得先重新校準——保留色相
 * （色相才是身分），把明度與彩度收進帶內。這件事還沒做，做之前這個檔只是倉庫。
 */

